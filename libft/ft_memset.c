#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int i;

	i = 0;
	while (i < len)
	{
		*(char *)b++ = (char)c;
		i++;
	}
	b = b - len;
	return (b);
}
