#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int a;
	int len;

	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		a = 0;
		while (a < len)
		{
			(*f)(s);
			s++;
			a++;
		}
	}
}
