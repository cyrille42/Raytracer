#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c;

	c = s1;
	while (*c != '\0')
	{
		c++;
	}
	while (*s2 != '\0' && n > 0)
	{
		*c = *s2;
		c++;
		s2++;
		n--;
	}
	*c = '\0';
	return (s1);
}
